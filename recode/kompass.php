<h2>Kompass</h2>
<p>Kompass (keskld. compassus – ring, sirkel) on navigatsiooniline mõõteriist, mille abil saab määrata ilmakaari.

    Kompassi osutusraami ringskaalale on nurgakraadides kantud tähistus põhiilmakaartega: põhi, lõuna, ida ja lääs ja enamasti ka vahekaartega, kagu, edel, loe, kirre.

    Kompassile on tavaliselt joonistatud kompassiroos ilmakaarte nimetustega, mis tavaliselt on lühendatud esitähtedeks: N, O, S, W. Mõnikord on selle asemel või lisaks sellele kompassile märgitud nurgakraadid. Nurki arvestatakse alates põhjasuunast päripäeva, nii et põhjasuunale vastab 0°, idasuunale 90°, lõunasuunale 180° ja läänesuunale 270.[1]</p>

